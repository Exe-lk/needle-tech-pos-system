/** Get expected machine categories for an agreement. */
function getExpectedCategories(agreement: RentalAgreement | null): ExpectedMachineCategory[] {
  if (!agreement) return [];
  const cats = agreement.expectedMachineCategories;
  if (cats && cats.length > 0) return cats;
  const total = agreement.expectedMachines ?? 0;
  if (total <= 0) return [];
  return [{ id: 'default', brand: '', model: 'Machine', type: '', quantity: total }];
}

/** QR contains only serial number and box number. Extract from JSON or key: value text. */
function extractSerialAndBoxFromQR(decodedText: string): { serial: string; box: string } | null {
  const raw = (decodedText || '').trim();
  if (!raw) return null;

  try {
    const parsed = JSON.parse(raw);
    if (parsed && typeof parsed === 'object') {
      const serialCandidates = [
        parsed.serialNo,
        parsed.serialNO,
        parsed.serial,
        parsed.serialNumber,
        parsed.SerialNo,
        parsed.Serial,
        parsed.machineSerial,
        parsed.machineSerialNo,
      ].filter((v: unknown) => typeof v === 'string' && (v as string).trim().length > 0);
      const boxCandidates = [
        parsed.motorBoxNo,
        parsed.boxNo,
        parsed.boxNumber,
        parsed.MotorBoxNo,
        parsed.BoxNo,
        parsed.box,
      ].filter((v: unknown) => typeof v === 'string' && (v as string).trim().length > 0);
      const serial = serialCandidates.length > 0 ? (serialCandidates[0] as string).trim() : '';
      const box = boxCandidates.length > 0 ? (boxCandidates[0] as string).trim() : '';
      if (serial || box) return { serial, box: box || '' };
    }
  } catch {
    // not JSON, try key: value pattern
  }

  const serialMatch = raw.match(/serial\s*(no|number)?\s*[:\-=]\s*([A-Za-z0-9\-_/]+)/i);
  const boxMatch = raw.match(/(?:motor)?\s*box\s*(no|number)?\s*[:\-=]\s*([A-Za-z0-9\-_/]+)/i);
  const serial = serialMatch ? serialMatch[2].trim() : '';
  const box = boxMatch ? boxMatch[2].trim() : '';
  if (serial || box) return { serial, box: box || '' };

  return null;
}

function getUpdateInitialData(agreement: RentalAgreement | null): Record<string, unknown> {
  if (!agreement) return {};
  return {
    agreementNo: agreement.agreementNo,
    customerName: agreement.customerName,
    startDate: agreement.startDate,
    endDate: agreement.endDate || '',
    monthlyRent: agreement.monthlyRent,
    status: agreement.status,
  };
}

