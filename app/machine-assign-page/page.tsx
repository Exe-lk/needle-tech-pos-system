type RentalStatus = 'Active' | 'Completed' | 'Cancelled' | 'Pending';

interface ExpectedMachineCategory {
  id: string;
  brand: string;
  model: string;
  type: string;
  quantity: number;
}

interface RentalAgreement {
  id: number;
  agreementNo: string;
  customerId?: string;
  customerNo: string;
  customerName: string;
  customerAddress?: string;
  serialNo: string;
  startDate: string;
  endDate: string | null;
  monthlyRent: number;
  outstanding: number;
  status: RentalStatus;
  rentalId?: string;
  purchaseRequestId?: number;
  purchaseRequestNumber?: string;
  expectedMachines?: number;
  addedMachines?: number;
  expectedMachineCategories?: ExpectedMachineCategory[];
}

interface MachineForAgreement {
  id: string;
  description: string;
  serialNumber: string;
  boxNumber: string;
  brandName?: string;
  modelName?: string;
  typeName?: string;
  scannedAt?: string;
  categoryIndex?: number;
}

// API response from GET /api/v1/rentals/by-number
interface RentalByNumberApiData {
  id: string;
  rentalId?: string;
  agreementNo: string;
  status: string;
  customerId?: string;
  customerName: string;
  customerAddress?: string;
  customerPhone?: string;
  customerEmail?: string;
  rentalStartDate: string;
  rentalEndDate: string | null; // null = open-ended
  monthlyRate: number;
  totalAmount: number;
  outstandingAmount: number;
  machines: { id: string; model: string; serialNumber: string; boxNumber?: string; description?: string }[];
  expectedMachines: number;
  addedMachines: number;
  expectedMachineCategories?: ExpectedMachineCategory[];
  purchaseRequestId?: string | null;
  purchaseRequestNumber?: string | null;
}

function mapBackendStatus(status: string): RentalStatus {
  const map: Record<string, RentalStatus> = {
    ACTIVE: 'Active',
    COMPLETED: 'Completed',
    CANCELLED: 'Cancelled',
    PENDING: 'Pending',
  };
  return map[status] ?? (status as RentalStatus);
}

function transformByNumberToAgreement(data: RentalByNumberApiData): RentalAgreement {
  const startDate =
    typeof data.rentalStartDate === 'string'
      ? data.rentalStartDate.split('T')[0]
      : new Date(data.rentalStartDate).toISOString().split('T')[0];
  const endDate =
    data.rentalEndDate == null || data.rentalEndDate === ''
      ? null
      : typeof data.rentalEndDate === 'string'
        ? data.rentalEndDate.split('T')[0]
        : new Date(data.rentalEndDate).toISOString().split('T')[0];
  return {
    id: 0,
    agreementNo: data.agreementNo,
    customerId: data.customerId,
    customerNo: '',
    customerName: data.customerName,
    customerAddress: data.customerAddress,
    serialNo: '',
    startDate,
    endDate: endDate ?? null,
    monthlyRent: data.monthlyRate ?? 0,
    outstanding: data.outstandingAmount ?? 0,
    status: mapBackendStatus(data.status),
    rentalId: data.rentalId,
    purchaseRequestNumber: data.purchaseRequestNumber ?? undefined,
    expectedMachines: data.expectedMachines,
    addedMachines: data.addedMachines,
    expectedMachineCategories: data.expectedMachineCategories,
  };
}

function transformApiMachinesToLocal(
  machines: RentalByNumberApiData['machines'],
  startCategoryIndex: number = 0
): MachineForAgreement[] {
  return machines.map((m, i) => ({
    id: m.id,
    description: m.description ?? `${m.serialNumber}${m.boxNumber ? ` · Box: ${m.boxNumber}` : ''}`,
    serialNumber: m.serialNumber,
    boxNumber: m.boxNumber ?? '',
    scannedAt: new Date().toISOString(),
    categoryIndex: startCategoryIndex,
  }));
}

